const insertFranchises = async (t: Transaction) => {
  try {
    let franchisesToInsert: FranchiseAttributes[] = [];

    await Promise.all(
      franchises.map(async (franchise) => {
        const franchiseInserted = await Franchises.findByPk(franchise.id);
        if (!franchiseInserted) {
          franchisesToInsert.push(franchise);
        }
      })
    );

    await Franchises.bulkCreate(franchisesToInsert, { transaction: t });

    global.logger.info(`Inserted franchises ${franchises.length} news`);
    return;
  } catch (error) {
    global.logger.error(error);
    throw error;
  }
};

